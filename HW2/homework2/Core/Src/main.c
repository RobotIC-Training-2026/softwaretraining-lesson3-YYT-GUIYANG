/**
  * @brief  解析数据包并发送回复（作业2：个数解析）
  * @param  data: 指向8字节数据包的指针，格式：CC DD ID 个数 距离1 距离2 距离3 距离4
  * @retval None
  */
void ParseAndSendResponse(uint8_t *data)
{
  char response[64];
  uint8_t i;
  
  // 解析数据包：CC DD ID 个数 距离1 距离2 距离3 距离4
  uint8_t id = data[2];
  uint8_t count = data[3];
  
  // 格式化输出：ID:XX 个数:X 距离:XX XX XX XX
  sprintf(response, "ID:%d 个数:%d 距离:", id, count);
  
  // 根据个数添加距离数据
  for (i = 0; i < 4; i++)
  {
    if (i < count)
    {
      char temp[8];
      sprintf(temp, "%d ", data[4 + i]);
      strcat(response, temp);
    }
    else
    {
      strcat(response, "0 ");
    }
  }
  strcat(response, "\r\n");
  
  // 发送回复
  HAL_UART_Transmit(&huart1, (uint8_t *)response, strlen(response), 1000);
}

